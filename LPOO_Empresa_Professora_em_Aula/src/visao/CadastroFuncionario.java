    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        if(!txtNome.getText().isEmpty()){
            fun.setNome(txtNome.getText());
            try {
                fun.setDataNascimento(sdf.parse(txtDtNasc.getText()));
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(rootPane, "Data inv치lida");
            }
            if(rdFem.isSelected()){
                fun.setSexo('S');
            } else if(rdMas.isSelected()){
                fun.setSexo('M');
            }
            fun.setSalarioHora(Double.parseDouble(txtSalario.getText().replace(',', '.')));
            
            //JOptionPane.showMessageDialog(rootPane, fun.getInfo());
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Campo nome obrigat칩rio");
        }
        
        
    }//GEN-LAST:event_btnSalvarActionPerformed

