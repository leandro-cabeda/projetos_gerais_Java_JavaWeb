    private void txtNomeBuscaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomeBuscaKeyReleased
        String nome = txtNomeBusca.getText().trim().toLowerCase();
        Empresa objSel = (Empresa) cmbEmpresaBusca.getSelectedItem();
        modeloTab.setNumRows(0);
        if(objSel == null){
            for(Departamento obj: (new DepartamentoDAO()).buscar(nome)){
                modeloTab.addRow(new Object[]{obj,
                    obj.getEmpresa()});
            }
        } else {
            for(Departamento obj: (new DepartamentoDAO()).buscar(objSel, nome)){
                modeloTab.addRow(new Object[]{obj,
                    obj.getEmpresa()});
            }
        }
        
    }//GEN-LAST:event_txtNomeBuscaKeyReleased

    private void cmbEmpresaBuscaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbEmpresaBuscaItemStateChanged
        Empresa objSel = (Empresa) cmbEmpresaBusca.getSelectedItem();
        String nome = txtNomeBusca.getText().trim().toLowerCase();
        modeloTab.setNumRows(0);
        if(objSel == null){
            carregarDepartamentos();
        } else {
            if(nome.isEmpty()){ //NÃ£o tem texto
                for(Departamento obj: (new DepartamentoDAO()).buscar(objSel)){
                    modeloTab.addRow(new Object[]{obj,
                        obj.getEmpresa()});
                }
            } else { //Tem texto e empresa Selecionada
                 for(Departamento obj: (new DepartamentoDAO()).buscar(objSel, nome)){
                    modeloTab.addRow(new Object[]{obj,
                        obj.getEmpresa()});
                }
            }
            
           
        }
        
    }//GEN-LAST:event_cmbEmpresaBuscaItemStateChanged


