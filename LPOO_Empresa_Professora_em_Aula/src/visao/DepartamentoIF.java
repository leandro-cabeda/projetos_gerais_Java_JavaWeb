    public void carregarEmpresas() {
        EmpresaDAO dao = new EmpresaDAO();
        cmbEmpresaBusca.removeAllItems();
        cmbEmpresa.removeAllItems();
        cmbEmpresaBusca.addItem(null);
        
        for (Empresa e : dao.buscar()) {
            cmbEmpresaBusca.addItem(e);
            cmbEmpresa.addItem(e);
        }
    }

    public void carregarDepartamentos() {
//        tblDepartamentos.removeAll();
        modeloTab.setNumRows(0);
        DepartamentoDAO dao = new DepartamentoDAO();
        for (Departamento obj : dao.buscar()) {
            modeloTab.addRow(new Object[]{obj,
                obj.getEmpresa()});
        }
    }
    
