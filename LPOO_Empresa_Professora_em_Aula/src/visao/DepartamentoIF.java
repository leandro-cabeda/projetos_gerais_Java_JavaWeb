    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Departamento obj = new Departamento();
        if (tblDepartamentos.getSelectedRow() == -1) {
            obj.setNome(txtNome.getText());
            obj.setEmpresa((Empresa) cmbEmpresa.getSelectedItem());
            (new DepartamentoDAO()).adicionar(obj);
            txtNome.setText("");
            carregarDepartamentos();
        } else {//editar
            obj = (Departamento) modeloTab.getValueAt(tblDepartamentos.getSelectedRow(), 0);
            obj.setNome(txtNome.getText());
            obj.setEmpresa((Empresa) cmbEmpresa.getSelectedItem());
            (new DepartamentoDAO()).editar(obj);
            txtNome.setText("");
            carregarDepartamentos();
        }
        limparBusca();

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Departamento objSel = (Departamento) modeloTab.getValueAt(tblDepartamentos.getSelectedRow(), 0);
        int opExc = JOptionPane.showConfirmDialog(rootPane,
                "Tem certeza que deseja excluir o departamento " + objSel.getNome() + "?");
        if (opExc == JOptionPane.YES_OPTION) {
            (new DepartamentoDAO()).excluir(objSel);
            carregarDepartamentos();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

