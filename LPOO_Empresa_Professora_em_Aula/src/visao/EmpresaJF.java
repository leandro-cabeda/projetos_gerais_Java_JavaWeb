    public void carregaEmpresas() {
        modelo.removeAllElements();
        EmpresaDAO dao = new EmpresaDAO();
        ArrayList<Empresa> lista = dao.buscar();
        for (Empresa obj : lista) {
            modelo.addElement(obj);
        }
    }

