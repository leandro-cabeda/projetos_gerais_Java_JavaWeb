    private void btnInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInfoActionPerformed
        Empresa objSel = lstEmpresa.getSelectedValue();
        if (objSel != null) {
            JOptionPane.showMessageDialog(rootPane, objSel.getInfo());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma empresa");
        }
    }//GEN-LAST:event_btnInfoActionPerformed

    private void txtNomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomeKeyReleased
        String nome = txtNome.getText();
        modelo.removeAllElements();
        EmpresaDAO dao = new EmpresaDAO();
        ArrayList<Empresa> lista = dao.buscar(nome);
        for (Empresa obj : lista) {
            modelo.addElement(obj);
        }
    }//GEN-LAST:event_txtNomeKeyReleased

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        CadastroEmpresa tela = new CadastroEmpresa(this, rootPaneCheckingEnabled);
        tela.setVisible(true);
        Empresa novoObj = tela.getEmpresa();
        if(novoObj != null){
            EmpresaDAO dao = new EmpresaDAO();
            dao.adicionar(novoObj);
            carregaEmpresas();
        }
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Empresa objSel = lstEmpresa.getSelectedValue();
        if (objSel != null) {
            int res = JOptionPane.showConfirmDialog(rootPane, "Deseja excluir cadastro da empresa "+objSel.getNome()+"?");
            if(res == JOptionPane.YES_OPTION){
                EmpresaDAO dao = new EmpresaDAO();
                dao.excluir(objSel);
                carregaEmpresas();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma empresa");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

