    private void nomebuscaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nomebuscaKeyReleased
        String nome=nomebusca.getText();
        DepartamentoDAO dao= new DepartamentoDAO();
        Empresa ob=(Empresa) listaempresa.getSelectedItem();
        modelotab.setNumRows(0);
        departamentos.removeAll();
        if(!nome.isEmpty())
        {
            for(Departamento obj: dao.buscardado(nome))
            {
                modelotab.addRow(new Object[]{obj,obj.getEmpresa()});
            }
        }
        else if(!nome.isEmpty() && ob!=null)
        {
            for(Departamento obj: dao.buscadepemp(nome,ob))
            {
                modelotab.addRow(new Object[]{obj,obj.getEmpresa()});
            }
            
        }
        else if(ob!=null)
        {
            for(Departamento obj: dao.buscarempresa(ob))
            {
             modelotab.addRow(new Object[]{obj,obj.getEmpresa()});
            }
        }
        else
        {
            carregarDepartamentos();
        }
    }//GEN-LAST:event_nomebuscaKeyReleased

