    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
      Departamento obj= new Departamento();
      
      if(departamentos.getSelectedRow() == -1)
      {
          if(empresa.getSelectedItem()!=null)
          {
                obj.setNome(nomedep.getText());
               obj.setEmpresa((Empresa)empresa.getSelectedItem());
               (new DepartamentoDAO()).adicionar(obj);
          }
          else
          {
              JOptionPane.showMessageDialog(null,"Selecionar empresa é obrigatório");
          }
          
         
      }
      else
      { 
          Departamento objSel =(Departamento)modelotab.getValueAt(departamentos.getSelectedRow(),0);
          nomedep.setText(objSel.getNome());
          objSel.setNome(nomedep.getText());
          objSel.setEmpresa((Empresa)empresa.getSelectedItem());
          (new DepartamentoDAO()).editar(objSel);
      }
      del.setVisible(false);
      nomebusca.setText("");
      nomedep.setText("");
      carregarDepartamentos();
      carregarEmpresas();
    }//GEN-LAST:event_salvarActionPerformed

    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed

        Departamento objSel =(Departamento)modelotab.getValueAt(departamentos.getSelectedRow(),0);
        int opexc= JOptionPane.showConfirmDialog(rootPane,"Tem certeza qeu deseja excluir o departamento "+objSel.getNome()+"?");
        if(opexc== JOptionPane.YES_OPTION)
        {
            (new DepartamentoDAO()).excluir(objSel);
            carregarEmpresas();
            carregarDepartamentos();
             nomebusca.setText("");
             nomedep.setText("");
        }

    }//GEN-LAST:event_delActionPerformed

