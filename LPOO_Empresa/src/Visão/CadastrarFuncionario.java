    public void carregarDepartamento() {
        DepartamentoDAO dao = new DepartamentoDAO();
        dep.removeAllItems();

        dep.addItem(null);
        for (Departamento e : dao.buscar()) {
            dep.addItem(e);

        }
    }

    public void carregarDados() {
        String sexo;

        if (f != null) {
            nome.setText(f.getNome());
            nasc.setText(sdf.format(f.getNasc()));
            altura.setText("" + f.getAltura());
            peso.setText("" + f.getPeso());
            horas.setText("" + f.getHoras());
            salariohora.setText("" + f.getSalarioHora());
            sexo = f.getSexo();
            if (sexo.equals("F")) {
                F.setSelected(true);
            } else if (sexo.equals("M")) {
                M.setSelected(true);
            }
            dep.setSelectedItem(f.getDepartamento());

           
        }

    }

