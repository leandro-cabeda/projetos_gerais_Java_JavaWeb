    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        Departamento ob = (Departamento) dep.getSelectedItem();
        String a = "";
        int i = 0;

        if (f == null) {
            f = new Funcionario();

        }
        if (nome.getText().isEmpty()) {
            a += "Campo nome obrigatório!!\n";
            i++;

        }
        if (!F.isSelected() && !M.isSelected()) {
            a += "Obrigatório selecionar tipo de sexo!!\n";
            i++;
        }
        if (nasc == null) {
            a += "Campo Data Nascimento é obrigatório!!\n";
            i++;
        }
        if (altura.getText().isEmpty()) {
            a += "Campo altura é obrigatório!!\n";
            i++;
        }
        if (peso.getText().isEmpty()) {
            a += "Campo peso é obrigatório!!\n";
            i++;
        }
        if (horas.getText().isEmpty()) {
            a += "Campo horas é obrigatório!!\n";
            i++;
        }
        if (salariohora.getText().isEmpty()) {
            a += "Campo Salário hora é obrigatório!!\n";
            i++;
        }
        if (ob == null) {
            a += "Campo Selecionar Departamento é obrigatório!!";
            i++;
        }
        if (i > 0) {
            JOptionPane.showMessageDialog(rootPane, a);
            nome.setText("");
            nasc.setText("");
            altura.setText("");
            peso.setText("");
            horas.setText("");
            salariohora.setText("");
            //this.remove(F); Remove o campo do radio
            //this.remove(M); Remove o campo do radio
            //this.initComponents(); Reativa de novo os componetes que foram removidos como fosse resetando
            mf.clearSelection();//Reseta os componentes do grupo radio e deixa desmarcado
            carregarDepartamento();
        } else {
            f.setNome(nome.getText().trim());

            try {
                f.setNasc(sdf.parse(nasc.getText()));
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(null, "Data inválida!!");
            }

            /*if (F.isSelected()) {
                f.setSexo("F");
            } else if (M.isSelected()) {
                f.setSexo("M");
            }*/
            f.setSexo(F.isSelected()?"F":"M");
            f.setSalarioHora(Double.parseDouble(salariohora.getText().trim()));
            f.setAltura(Double.parseDouble(altura.getText().replace(',', '.').trim()));
            f.setPeso(Double.parseDouble(peso.getText().replace(',', '.').trim()));
            f.setHoras(Double.parseDouble(horas.getText().replace(',', '.').trim()));
            f.setDepartamento(ob);
            this.dispose();
        }
    }//GEN-LAST:event_salvarActionPerformed

